def intersect (se1,se2):
    res=[]
    for a in se1:
            if a in se2:
                    res.append(a)
    
    return res
def notin(seq1,seq2):
    res=[]
    for a in seq1:
            if a not in seq2:
                    res.append(a)
    
    return res
